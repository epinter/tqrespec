package br.com.pinter.tqrespec.gui;

import java.nio.file.Path;

public class CustomMap {
    private String name;
    private Path path;

    public CustomMap(String name, Path path) {
        this.name = name;
        this.path = path;
    }

    public String getName() {
        return name;
    }

    public Path getPath() {
        return path;
    }

    @Override
    public String toString() {
        return name;
    }
}
